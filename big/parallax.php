<p>Parallax website are one of the most popular trends in web design the past few year. It controls how a user read the site by revealing the page in a one to one manner. Materializecss.com states “Parallax is an effect where the background content or image in this case, is moved at a different speed than the foreground content while scrolling.” They are extremely impactful if used correctly, but should be used sparingly as with any ‘trend’. They have become somewhat overused in the past few years making them lose some of their wow factor.</p>
<p>Parallax site would be a poor choice for a page the display a lot of text because the background would really distracting and make the text hard to read. It’s a little like the Starwars text, if they intended us to read it why is it scrolling away? They also can be expensive and time consuming. Aniruddha Mysore at beebom.com says “Making a parallax scrolling site is often tedious because it requires in-depth knowledge of CSS, Javascript  and good web design to pull off.”
</p>
<p>The type of website best suited for the parallax effect would be some type of marketing like when you are announcing something. They remind me of the opening credits to the movie, you might not understand what is going on, yet the imagery mixed with the text in visually interesting. You want to have the person's attention before arriving at the site because it will take some time to load. Sufyan bin Uzayr from envato.com says “If your website fails to load fast, it will lose visitors, no matter how well designed it may be.”</p>
<p>For small companies I think it is best to stay away from a parallax site unless it is really necessary. I someone visits the site often the effect will be more annoying the more time they have to interact with it.
</p>
